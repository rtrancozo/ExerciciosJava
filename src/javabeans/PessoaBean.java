package javabeans;

public class PessoaBean { // java bean é uma classe que tem seus componentes encapsulados através dos modificadores de acesso.
    private String nome;
    private int idade;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
